    public static class Node{
        public int value;
        public Node next;
        public Node(int v){
            this.value = v;
        }
    }

    // need n extra space
    public static boolean isPalindrome1(Node head) {
        Stack<Node> stack = new Stack<Node>();
        Node cur = head;
        while (cur != null) {
            stack.push(cur);
            cur = cur.next;
        }
        while (head != null) {
            if (head.value != stack.pop().value) {
                return false;
            }
            head = head.next;
        }
        return true;
    }

    // need n/2 extra space
    public static boolean isPalindrome2(Node head) {
        if (head == null || head.next == null) {
            return true;
        }
        Node right = head.next;
        Node cur = head;
        while (cur.next != null && cur.next.next != null) {
            right = right.next;
            cur = cur.next.next;
        }
        Stack<Node> stack = new Stack<Node>();
        while (right != null) {
            stack.push(right);
            right = right.next;
        }
        while (!stack.isEmpty()) {
            if (head.value != stack.pop().value) {
                return false;
            }
            head = head.next;
        }
        return true;
    }
    public static boolean isPalindrome3(Node head){
        // 快慢指针定位到奇数的中点，或者偶数的下节点，返回slow指针。
        if (head == null || head.next == null) {
            return true;
        }
        Node n1 = head;
        Node n2 = head;
        while (n2.next != null && n2.next.next != null) { // find mid node
            n1 = n1.next; // n1 -> mid
            n2 = n2.next.next; // n2 -> end
        }
        // 调整slow节点后的链表顺序
        Node split = n1.next;
        n1.next = null;
        Node pre = n1;
        Node next = null;
        while(split != null){
            next = split.next;
            split.next = pre;
            pre = split;
            split = next;
        }
        Node tail = pre;
        // 头尾指针同时移动进行比较判断，尾指针到达slow指针的位置比较结束。
        Node cur1 = head;
        Node cur2 = tail;
        boolean res = true;
        while (cur1 != null && cur2 !=null){
            if (cur1.value != cur2.value){
                res = false;
                break;
            }
            cur1 = cur1.next;
            cur2 = cur2.next;
        }
        Node Pre = null;
        Node Next = null;
        // 还原回原链表
        while(tail != null){
            Next = tail.next;
            tail.next = Pre;
            Pre = tail;
            tail = Next;
        }
        return res;
    }
